public class NabsPattern {
    public static void main(String[] args) {
    int a=0, row,col;
    for(row=1;row<=4;row++){
    for(col=1;col<=row;col++){
        System.out.print(a);
        a++;
    }    System.out.println(); 
    }
    }  
}
